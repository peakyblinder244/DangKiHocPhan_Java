    private void themGiaoVienActionPerformed(java.awt.event.ActionEvent evt) {
        String strNull="";
        String maGiaoVien = layMaGiaoVien.getText();
        String tenGiaoVien =  layTenGiaoVien.getText();
        String gioiTinh = layGioiTinh.getSelectedItem().toString();
        String ngaySinh = layNgaySinh.getText();
        String diaChi = layDiaChi.getText();
        String maMonHoc = layMonHoc.getText();
        if(maGiaoVien.equals(strNull) || tenGiaoVien.equals(strNull) || gioiTinh.equals(strNull) || ngaySinh.equals(strNull)){
            JOptionPane.showMessageDialog(null, "Không Được Để Trống Các Ô Dữ Liệu !");
            this.ResetForm();
            return;
        }
        if(ngaySinh.charAt(4) != '-'|| ngaySinh.charAt(7) != '-' ){
            JOptionPane.showMessageDialog(null, "Ngày sinh nhập sai !!");
            this.ResetForm();
            return;
        }
        Date date = Date.valueOf(ngaySinh);
        Monhoc monhoc = giaoVuService.layThongTinMonHocBangMaMonHoc(maMonHoc);
        if(monhoc == null){
            JOptionPane.showMessageDialog(null, "Không Tồn Tại Môn Học !! Mời Kiểm Tra Lại");
            this.ResetForm();
            return;
        }
        Giaovien giaovienMoi = new Giaovien(maGiaoVien,tenGiaoVien,gioiTinh,date,diaChi,monhoc);
        if(giaoVuService.themGiaoVien(giaovienMoi)){
            JOptionPane.showMessageDialog(this, "Thêm Thành Công ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            capNhatDanhSachGiaoVien();
        }
        else{
            JOptionPane.showMessageDialog(null, "Lỗi Không Thêm Được Mời Kiểm Tra Lại Dữ Liệu !");
            this.ResetForm();
        }
    }

    private void xoaGiaoVienActionPerformed(java.awt.event.ActionEvent evt) {
        if(giaovienChon == null){
            JOptionPane.showMessageDialog(null, "Bạn Chưa Chọn Giáo Viên Cần Xóa!");
            this.ResetForm();
        }
        else{
            if(giaoVuService.xoaGiaoVien(giaovienChon)){
                JOptionPane.showMessageDialog(this, "Xóa  Thành Công ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                giaovienChon = null;
                capNhatDanhSachGiaoVien();
            }
            else{
                JOptionPane.showMessageDialog(null, "Xóa Không Thành Công!! Mời Kiểm Tra Lại Dữ Liệu");
                this.ResetForm();
            }
        }
    }

    private void lamMoiActionPerformed(java.awt.event.ActionEvent evt) {
        capNhatDanhSachGiaoVien();
        ResetForm();
    }
    private void ResetForm(){
        layMaGiaoVien.setText("");
        layTenGiaoVien.setText("");
        layGioiTinh.setSelectedIndex(0);
        layNgaySinh.setText("");
        layDiaChi.setText("");
        layMonHoc.setText("");
    }

    void capNhatDanhSachGiaoVien(){
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        danhSachGiaoVien.setModel(defaultTableModel);
        defaultTableModel.addColumn("Mã Giáo Viên");
        defaultTableModel.addColumn("Tên Tên Giáo Viên");
        defaultTableModel.addColumn("Giới Tính");
        defaultTableModel.addColumn("Ngày Sinh");
        defaultTableModel.addColumn("Địa Chỉ");
        defaultTableModel.addColumn("Mã Môn Học");
        List<Giaovien> danhSachGiaoVienDay = giaoVuService.layDanhSachGiaoVien();
        for (Giaovien i: danhSachGiaoVienDay) {
            Object[] tmp = new Object[]{i.getMaGiaoVien(), i.getTenGiaoVien(), i.getGioiTinh(), i.getNgaySinh().toString(), i.getDiaChi(),i.getMonHoc().getMaMonHoc()};
            defaultTableModel.addRow(tmp);
        }
    }

