    private void danhSachGiaoVienMouseClicked(java.awt.event.MouseEvent evt) {
        int dong = danhSachGiaoVien.getSelectedRow();
        int[] cot = danhSachGiaoVien.getSelectedColumns();
        String maGiaoVien = String.valueOf(danhSachGiaoVien.getValueAt(dong, 0));
        String tenGiaoVien =  String.valueOf(danhSachGiaoVien.getValueAt(dong, 1));
        String gioiTinh = String.valueOf(danhSachGiaoVien.getValueAt(dong, 2));
        String ngaySinh = String.valueOf(danhSachGiaoVien.getValueAt(dong, 3));
        String diaChi = String.valueOf(danhSachGiaoVien.getValueAt(dong, 4));
        String maMonHoc = String.valueOf(danhSachGiaoVien.getValueAt(dong, 5));

        layMaGiaoVien.setText(maGiaoVien);
        layTenGiaoVien.setText(tenGiaoVien);
        if(gioiTinh.equals("Nam")){
            layGioiTinh.setSelectedIndex(0);
        }
        else{
            layGioiTinh.setSelectedIndex(1);
        }
        layNgaySinh.setText(ngaySinh);
        layDiaChi.setText(diaChi);
        layMonHoc.setText(maMonHoc);
        giaovienChon = giaoVuService.layGiaoVienBangMaGiaoVien(maGiaoVien);
    }

