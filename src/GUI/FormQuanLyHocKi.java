    private void themHocKiActionPerformed(java.awt.event.ActionEvent evt) {
        String strNull = "";
        String tenHocKi = layTenHocKi.getText();
        String strNamHoc = layNamHoc.getText();
        String ngayBatDau = layNgayBatDau.getText();
        String ngayKetThuc = layNgayKetThuc.getText();
        int namHoc = 0;
        try {
            namHoc = Integer.parseInt(strNamHoc);
        }catch (Exception e){
            System.err.println(e);
            namHoc = 0;
        }
        if(tenHocKi.equals(strNull) || strNamHoc.equals(strNull) || ngayBatDau.equals(strNull) || ngayKetThuc.equals(strNull)) {
            JOptionPane.showMessageDialog(null, "Không Được Để Trống Các Ô Dữ Liệu!!");
            this.ResetForm();
            return;
        }
        if(ngayBatDau.charAt(4) != '-'|| ngayKetThuc.charAt(7) != '-' ){
            JOptionPane.showMessageDialog(null, "Ngày sinh nhập sai !!");
            this.ResetForm();
            return;
        }
        Date dateBatDau = Date.valueOf(ngayBatDau);
        Date dateKetThuc = Date.valueOf(ngayKetThuc);
        Hocki hockiMoi = new Hocki(tenHocKi,namHoc,dateBatDau,0,dateKetThuc);

        if(giaoVuService.themHocKiMoi(hockiMoi)){
            JOptionPane.showMessageDialog(this, "Thêm Thành Công ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            capNhatDanhSachHocKi();
        }
        else{
            JOptionPane.showMessageDialog(null, "Lỗi Không Thêm Được Mời Kiểm Tra Lại Dữ Liệu !");
            this.ResetForm();
        }
    }

    private void suaHocKiActionPerformed(java.awt.event.ActionEvent evt) {
        if(hockiChon == null){
            JOptionPane.showMessageDialog(null, "Bạn Chưa Chọn Môn Học Cần Xóa!");
            this.ResetForm();
        }
        else {
            String strNull = "";
            String tenHocKi = layTenHocKi.getText();
            String strNamHoc = layNamHoc.getText();
            String ngayBatDau = layNgayBatDau.getText();
            String ngayKetThuc = layNgayKetThuc.getText();
            int namHoc = 0;
            try {
                namHoc = Integer.parseInt(strNamHoc);
            } catch (Exception e) {
                System.err.println(e);
                namHoc = 0;
            }
            if (tenHocKi.equals(strNull) || strNamHoc.equals(strNull) || ngayBatDau.equals(strNull) || ngayKetThuc.equals(strNull)) {
                JOptionPane.showMessageDialog(null, "Không Được Để Trống Các Ô Dữ Liệu!!");
                this.ResetForm();
                return;
            }
            if (ngayBatDau.charAt(4) != '-' || ngayKetThuc.charAt(7) != '-') {
                JOptionPane.showMessageDialog(null, "Ngày sinh nhập sai !!");
                this.ResetForm();
                return;
            }
            Date dateBatDau = Date.valueOf(ngayBatDau);
            Date dateKetThuc = Date.valueOf(ngayKetThuc);
            Hocki hockiSua = giaoVuService.layThongTinHocKiHienTai(tenHocKi,namHoc);
            hockiSua.setNgayBatDau(dateBatDau);
            hockiSua.setNgayKetThuc(dateKetThuc);
            if (giaoVuService.capNhatHocKi(hockiSua)) {
                JOptionPane.showMessageDialog(this, "Cập Nhật Thành Công ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                capNhatDanhSachHocKi();
            } else {
                JOptionPane.showMessageDialog(null, "Lỗi Không Cập Nhật Được Mời Kiểm Tra Lại Dữ Liệu !");
                this.ResetForm();
            }
        }
    }

