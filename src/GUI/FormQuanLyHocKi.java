    private void setHocKiHienTaiActionPerformed(java.awt.event.ActionEvent evt) {
        if(hockiChon == null){
            JOptionPane.showMessageDialog(null, "Bạn Chưa Chọn Học Kì Set");
            this.ResetForm();
        }
        else {
            List<Hocki> hockis = giaoVuService.layDanhSachHocKi();
            for (Hocki i : hockis) {
                i.setSetHientai(0);
                giaoVuService.capNhatHocKi(i);
            }
            hockiChon.setSetHientai(1);
            if (!giaoVuService.capNhatHocKi(hockiChon)) {
                JOptionPane.showMessageDialog(null, "Set Học Kì Thất Bại!! Mời Kiểm Tra Lại !!");
                this.ResetForm();
            } else {

                JOptionPane.showMessageDialog(this, "Set Học Kì Thành Công ", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }

    private void danhSachHocKiMouseClicked(java.awt.event.MouseEvent evt) {
        int dong = danhSachHocKi.getSelectedRow();
        int[] cot = danhSachHocKi.getSelectedColumns();
        String tenHocKi = String.valueOf(danhSachHocKi.getValueAt(dong, 0));
        int namHoc = (int) (danhSachHocKi.getValueAt(dong, 1));
        String ngayBatDau = String.valueOf(danhSachHocKi.getValueAt(dong, 2));
        String ngayKetThuc = String.valueOf(danhSachHocKi.getValueAt(dong, 3));


        hockiChon = giaoVuService.layThongTinHocKiHienTai(tenHocKi,namHoc);

        System.out.println(hockiChon.toString());
        if (hockiChon != null) {
            layTenHocKi.setText(hockiChon.getTenHocKi());
            layNamHoc.setText(hockiChon.getNamHoc().toString());
            layNgayBatDau.setText(hockiChon.getNgayBatDau().toString());
            layNgayKetThuc.setText(hockiChon.getNgayKetThuc().toString());
        }
    }


    private void capNhatDanhSachHocKi(){
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        danhSachHocKi.setModel(defaultTableModel);
        defaultTableModel.addColumn("Tên Học Kì");
        defaultTableModel.addColumn("Năm Học");
        defaultTableModel.addColumn("Ngày Bắt Đầu");
        defaultTableModel.addColumn("Ngày Kết Thúc");
        List<Hocki> listHocKi = giaoVuService.layDanhSachHocKi();
        for (Hocki i : listHocKi) {
            Object[] tmp = new Object[]{i.getTenHocKi(), i.getNamHoc(), i.getNgayBatDau().toString(),i.getNgayKetThuc().toString()};
            defaultTableModel.addRow(tmp);
        }
    }

